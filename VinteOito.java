


public class VinteOito {
	
	public static void main (String[] args) {
		/*Nathan Henrique Paiva Bonifacio*/
		
		int num = 1;
		for(int i = 1; i <= 10; i++)
		{
			System.out.print(i + ", ");
			for(int aux = num + i ; aux <= 10 ; aux++)
			{
				System.out.print(aux + " ");
			}
			System.out.println();
		}
	}
}

