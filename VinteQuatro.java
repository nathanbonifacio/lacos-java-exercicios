


public class VinteQuatro {
	
	public static void main (String[] args) {
		/*Nathan Henrique Paiva Bonifacio*/
		
		int mult = 0;
		for(int i = 1; i <= 10; i++)
		{
			System.out.println();
			for(int num = 0; num <= 10; num++){
				mult = i * num;
				System.out.println(i + " X " + num + " = " + mult);
			}
		}
	}
}

